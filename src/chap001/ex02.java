		//배열 - 같은 타입의 값 or 객체(주소)를 모아놓은 구조 (타입)
		int[] int_arr = new int[3]; //기본적인 객체생성,선언 (초기화)
		//	 0   0   0     -> 0 - int 타입을 저장할수 있는 메모리공간 =int를 저장할수있는 공간을 만드는것   
	//index  0   1   2 		->배열의 인덱스(주소,자리,몇번째 공간)
   //**index 0   1   2 -> 중요 0부터시작
		
		//값  1   2   3   
