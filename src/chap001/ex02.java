		//이차원배열
		//일차원배열 -> 일렬로
		// 0 0 0 0 -> 열
		
		//이차원배열 -> 일차원 배열을 여러개 저장할 수 있는 공강
		// 0 0 0 -> int_arr
		// 0 0 0 -> 같은 열 이모여서 행 열을 이루는 구조다
		
		//1번
		int[][] int_arr2 = new int[3][];  // 앞에 [] 의 크기가 정해져야 된다 -> 아래랑같다
		//2번
		int[] int_arr3 = new int[3]; // 배열의 크기가 정해져야한다
		
		
		//2번 -> 정수를 여러개 모아서 저장 -> 정수3개저장할수 있는 공간이 있다
		//1번 -> 정수'배열'을 여러개 모아서 저장 (배열들이 타입이 되는 느낌) -> 배열 3개를 저장할수있다
		
		//이차원배열 [행][열]
		//a~c 일차원 배열
		//a -> 1  2  3  4
		//b -> 5  6  7  8
		//c -> 9  10
		
