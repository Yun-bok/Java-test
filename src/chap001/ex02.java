		//int[][] test = new int[3][];
		//	0  0  0
		//	a  b  c  가들어 갈수도있다 타입은 배열로
		// 0번 1번 2번  -> idx
		// 0번 -> 배열	1 2 3 4 가들어있다
		// 			    0 1 2 3 
		//[0][2] -> 3값
		
		
		//[][] 앞 에 0 1 2 뒤에 a 면 1 2 3 4 b는 5 6 7 8
		//0 -> a(1 2 3 4)
		//1 -> b(5 6 7 8)
		//2 -> c(9 10)
		//[0][2] -> 3값
		
